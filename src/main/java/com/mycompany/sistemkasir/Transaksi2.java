    public int hitungTotalItem(){
        int totalItem = 0;
        DefaultTableModel model = (DefaultTableModel) tabelListBarang.getModel();
        int rowCount = model.getRowCount();
        for(int row=0; row<rowCount; row++){
            int total = Integer.valueOf(model.getValueAt(row, 5).toString());
            totalItem += total;
        }
        return totalItem;
    }
    public double hitungTotalBelanja(){
        double totalBelanja = this.hitungTotalItem() - this.hitungTotalItem()*this.discount;
        return totalBelanja;
    }
    
