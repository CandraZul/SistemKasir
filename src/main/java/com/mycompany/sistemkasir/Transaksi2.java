    private void inputIdMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputIdMemberActionPerformed
        try{
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM member";
            ResultSet result = statement.executeQuery(sql);
            boolean userFound = false;
            discount = 0;
            while(result.next()){
                if(result.getString("id").equals(inputIdMember.getText())){
                    discount = 10./100;
                    break;
                }           
            }
        }catch(SQLException e){
            System.out.println(e);
        }
        NumberFormat percentFormat = NumberFormat.getPercentInstance();
        inputDiscount.setText(percentFormat.format(discount));
        inputTotalBelanja.setText(String.valueOf(this.hitungTotalBelanja()));
    }//GEN-LAST:event_inputIdMemberActionPerformed

