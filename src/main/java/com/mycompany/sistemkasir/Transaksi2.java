    private void buttonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariActionPerformed
        kodeBarang = inputKodeBarang.getText();
        
        
        try{
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM data_barang WHERE kode_barang="+kodeBarang;
            ResultSet result = statement.executeQuery(sql);
            
            while(result.next()){
                namaBarang = result.getString("nama_barang");
                merk = result.getString("merk");
                hargaJual = result.getInt("harga_jual");
                stock = result.getInt("quantity");
            }
            inputNamaBarang.setText(namaBarang);
            inputMerk.setText(merk);
            inputHarga.setText(Integer.toString(hargaJual));
        }catch(SQLException e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_buttonCariActionPerformed

